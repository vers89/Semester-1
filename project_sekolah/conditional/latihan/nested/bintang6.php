<?php

for($a=1; $a<=5; $a++){
    echo "<br>";
    for($i=5; $i>=$a; $i--){
        echo "&nbsp";
    }
    for($g=1; $g<=$a; $g++){
        echo "*";
    }
}

for($t=4; $t>=1; $t--){
    echo " <br>";
    for($h=5; $h>=$t; $h--){
        echo "&nbsp";
    }
    for($n=1; $n<=$t; $n++){
        echo "*";
    }
}